        JScrollPane scroll = new JScrollPane(table); scroll.setBounds(50, 220, 550, 150); add(scroll);

        // Total
        lTotal = new JLabel("Total Amount:"); lTotal.setBounds(50, 380, 100, 25); add(lTotal);
        tTotal = new JTextField("0.00"); tTotal.setBounds(180, 380, 150, 25); tTotal.setEditable(false); add(tTotal);

        // Buttons
        bGenerate = new JButton("Generate & Print Bill"); bGenerate.setBounds(50, 420, 250, 30); add(bGenerate);
        bBack = new JButton("Back"); bBack.setBounds(320, 420, 120, 30); add(bBack);

        bGenerate.addActionListener(this);
